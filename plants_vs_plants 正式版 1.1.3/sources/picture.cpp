void draw(char *p,int top_left_x,int top_left_y,int picture_height,int picture_width,int color)
{
	int i,j;
	Color(color);
	for(i=0;i<picture_height;i++)
		for(j=0;j<picture_width;j++)
			if(*p++=='*')
			{
				Place(top_left_x+i,top_left_y+j);printf(" ");
			}
		return;
}

