void print_single_word(int color,int x,int y,int n,char (*kind)[Word_Height][Word_Width+1])
{
	int i,j;
	char (*p)[Word_Width+1]=kind[n];
	char *q;
	Color(color);
	for(i=0;i<Word_Height;i++,p++)
		for(j=0,q=p[0];j<Word_Width;j++,q++)
		    if(*q=='*')
		    {
				Place(x+i,y+2*j);
				printf("  ");//两格 
			}
	return; 
}
void print_sign(int color,int x,int y,int c)
{
	char character[]={'+','-','*','d','/','\\','=','.','e'/*ellipsis*/,'%','^','!','?','#','$','(',')','[',']','{','}','u'/*上*/,'b'/*下*/,'l'/*左*/,'r'/*右*/,'|','\''};
	int i;
	for(i=0;i<27;i++)
	    if(c==character[i])
	    {
			print_single_word(color,x,y,i,sign);
			break;
		}
	return;      
}
void print_big_number(int color,int x,int y,int m)
{
	if(m<0)
    {
		m=-m;
		print_sign(color,x,y,'-');
	}
	else
	    y-=(2*Word_Width+2);//抵消首个 
	const int MaxN=16;
	int M[MaxN];
	int i=0;
	do
		M[i++]=m%10;
	while((m/=10)!=0);
	while(--i>=0)
	    print_single_word(color,x,y+=(2*Word_Width+2),M[i],number);
	return; 	
}
void print_long_word(int color,int x,int y,char *s)
{
	y-=(2*Word_Width+2);//抵消首个 
	while(*s!='\0')
	{
	    y+=(2*Word_Width+2);
		if(*s!=' ')
	        if(  *s-'A'>=0  &&  *s-'Z'<=0  )
	            print_single_word(color,x,y,*s-'A',letter);
	        else if(  *s-'0'>=0  &&  *s-'9'<=0  )
	            print_single_word(color,x,y,*s-'0',number);
	        else
	            print_sign(color,x,y,*s);
	    ++s;
    }
	return; 	
}


